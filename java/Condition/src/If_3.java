import java.util.Scanner;

public class If_3 {
  public static void main(String[] args) {
    int num = 5;
    //if else문을 사용하면 둘 중 하나만 반드시 실행!

    //num이 10보다 크면 1을 출력
    if (num > 10) {
      System.out.println(1);
    } else {
      //조건이 만족하지 않으면 2를 출력
      System.out.println(2);


    }
  }
}
