public class Switch_2 {
  public static void main(String[] args) {

    String phone = "ios";

    switch (phone){
      case "android" :
        System.out.println(1);
        break;
      case "ios" :
        System.out.println(2);
        break;
      default :
        System.out.println("잘못된 데이터 입니다.");


    }
  }
}
