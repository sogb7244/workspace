public class While_1 {
  public static void main(String[] args) {

    int num = 1;



    //num이 10보다 작은 동안 반복
    while (num <= 5) {
      System.out.println(1);
      System.out.println(2);
      num++;
    }
    /// ////////////////////////////////////////////for 문
     for(int i = 0; i<= 5; i++){
       System.out.println(1);
       System.out.println(2);
    }



  }
}




