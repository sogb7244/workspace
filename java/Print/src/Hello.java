//자바의 모든 내용은 클래스 안에 작성한다
//클래스 안에는 반드시 main 메서드가 존재해야 한다.
//우리가 작성하는 코드는 반드시 main 메서드 안에 작성한다.
//코드 해석은 main 메서드 안의 첫줄부터 차례로 해석함.
//명령어가 끝나면 반드시 세미콜론을 붙인다.
public class Hello{
  public static void main(String[] args){ // main 메서드
    //출력문. 소괄호 안의 내용을 출력 + 한 줄 개행
    //문자열를 출력할 때에는 반드시 쌍따옴표에 감싸야한다.
    //문자 : 한 글자, 문자열 : 한 글자 이상의 글자
    System.out.println("hello~");
    System.out.println("A");

    //소괄호 안의 내용을 출력
    System.out.print("hi");
    System.out.print("hi");
    System.out.println("hi");

    //숫자는 쌍따옴표에 감싸지 않는다.
    System.out.println(5);
    System.out.println("5");

  }
}









